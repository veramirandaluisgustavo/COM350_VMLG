public class Coche {
    String nombre,modelo,marca,placa;
    int cilindrada;

    public Coche(String nombre, String modelo, String marca,String placa, int cilindrada) {
        this.nombre = nombre;
        this.modelo = modelo;
        this.marca = marca;
        this.placa = placa;
        this.cilindrada = cilindrada;
    }

    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    
    
    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public int getCilindrada() {
        return cilindrada;
    }

    public void setCilindrada(int cilindrada) {
        this.cilindrada = cilindrada;
    }
    
    
    
}
